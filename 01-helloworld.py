# 1. Print function
print("Hello World!")

# default of sep is " "
print("Hello" , "World!", sep="*")  # Hello * World


# default of end is "\n" -> break line
print("Hello World!", end=" ")

print("Same Line")




# 2. Basic Operations
print(1+1) # result: 2

print(1-1)  # 0

print(5 * 1)  # 5

print(3/2)  # 1.0

print(3//2)  # floor division

# Power
# 2 ^ 3 = 2 * 2 * 2
print(2 ** 3)  # 8

